export function getTheme(darkMode) {
  return darkMode ? graphite.dark : graphite.light;
}

export function getStatusStyle(status, darkMode) {
  const t = getTheme(darkMode);
  const styles = {
    New: { background: darkMode ? "rgba(96, 165, 250, 0.15)" : "#DBEAFE", color: t.accent },
    Contacted: { background: darkMode ? "rgba(148, 163, 184, 0.2)" : "#E2E8F0", color: t.primary },
    Qualified: { background: darkMode ? "rgba(203, 213, 225, 0.15)" : "#F1F5F9", color: darkMode ? "#CBD5E1" : "#475569" },
    Proposal: { background: t.warningBg, color: t.warning },
    Won: { background: t.successBg, color: t.success },
    Lost: { background: darkMode ? "rgba(239, 68, 68, 0.15)" : "#FEE2E2", color: t.danger },
  };
  return styles[status] || { background: t.surface, color: t.primary };
}
